package com.ibm.jaxws.tutorial.service.bean;

public class OrderItem {

	private String itemId;

	private int qty;

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public int getQty() {
		return qty;
	}

	public void setQty(int qty) {
		this.qty = qty;
	}

}
